-- Major sports events feed (marathons, Grand Slams, cycling, F1, etc.).
--
-- Rows are populated by:
--   1. The seed block at the bottom of this migration (so the Events UI
--      always has data to render, even before the edge function runs).
--   2. The `events-sync` Edge Function, which pulls fresh data daily from
--      TheSportsDB, OpenF1 and Wikidata SPARQL and upserts rows here.
--
-- The frontend reads from this table via the anon role; only the service
-- role can write.

CREATE TABLE IF NOT EXISTS public.external_events (
  id TEXT PRIMARY KEY,
  source TEXT NOT NULL,
  source_id TEXT NOT NULL,
  title TEXT NOT NULL,
  sport TEXT NOT NULL DEFAULT '',
  category TEXT NOT NULL DEFAULT '',
  country TEXT NOT NULL DEFAULT '',
  city TEXT NOT NULL DEFAULT '',
  venue TEXT NOT NULL DEFAULT '',
  starts_at TIMESTAMPTZ NOT NULL,
  ends_at TIMESTAMPTZ,
  url TEXT NOT NULL DEFAULT '',
  image_url TEXT NOT NULL DEFAULT '',
  description TEXT NOT NULL DEFAULT '',
  last_synced_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT external_events_source_id_unique UNIQUE (source, source_id)
);

CREATE INDEX IF NOT EXISTS external_events_starts_at_idx
  ON public.external_events (starts_at);
CREATE INDEX IF NOT EXISTS external_events_sport_idx
  ON public.external_events (sport);
CREATE INDEX IF NOT EXISTS external_events_country_idx
  ON public.external_events (country);

ALTER TABLE public.external_events ENABLE ROW LEVEL SECURITY;

-- Public read: anyone (anon or authenticated) can read major events.
DROP POLICY IF EXISTS "external_events_public_read" ON public.external_events;
CREATE POLICY "external_events_public_read" ON public.external_events
  FOR SELECT USING (TRUE);

-- Writes are restricted to the service_role (used by the events-sync edge
-- function) — no client-side inserts or updates.
GRANT SELECT ON public.external_events TO anon, authenticated;
GRANT SELECT, INSERT, UPDATE, DELETE ON public.external_events TO service_role;

-- Keep updated_at fresh on every UPDATE.
CREATE OR REPLACE FUNCTION public.touch_external_events_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at := NOW();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS external_events_touch_updated_at ON public.external_events;
CREATE TRIGGER external_events_touch_updated_at
  BEFORE UPDATE ON public.external_events
  FOR EACH ROW
  EXECUTE FUNCTION public.touch_external_events_updated_at();

